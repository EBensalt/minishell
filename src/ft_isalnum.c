int	ft_isalnum(int c)
{
	if (c != 9 && c != 32 && c != 0 && c != '"' && c != '$')
		return (1);
	return (0);
}

int	ft_isalnum_0(int c)
{
	if (c != 9 && c != 32 && c != 0 && c != '\'' && c != '"'
		&& c != '>' && c != '<' && c != '|' && c != '$')
		return (1);
	return (0);
}
