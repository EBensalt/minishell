#include <stdio.h>

int	ft_isalnum(int c)
{
	if (c != 9 && c != 32 && c != 0 && c != '"' && c != '$')
		return (1);
	return (0);
}

int	ft_isalnum_0(int c)
{
	if (c != 9 && c != 32 && c != 0
		&& c != '>' && c != '<' && c != '|')
		return (1);
	return (0);
}

int	ft_isalnum_1(int c)
{
	if (c != 9 && c != 32 && c != 0 && c != '\'' && c != '"'
		&& c != '>' && c != '<' && c != '|' && c != '$' && c != '?')
		return (1);
	return (0);
}

int	ft_isalnum_2(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z')
		|| (c >= '0' && c <= '9') || c == '_' || c == '+')
		return (1);
	return (0);
}
